def morse(msg):
  words=("".join(msg).upper()).split(" ")
  dcode=[]
  for word in words:
    letters=word.split("/")
    for letter in letters:
      dcode.append(MORSE_CODE_DICT[letter])
    dcode.append(" ")
  return "".join(dcode)